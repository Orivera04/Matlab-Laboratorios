% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
if ((isempty((get(handles.edit3,'String')))) ~= 1 & isempty((get(handles.edit8,'String'))) ~= 1 & isempty((get(handles.edit10,'String')) ) ~= 1)
axes(handles.axes1)
Funcion = (get(handles.edit3,'String'))
ezplot(Funcion)
A = eval((get(handles.edit8,'String')))
B = eval((get(handles.edit10,'String')))
N = eval((get(handles.edit12,'String')))
if(N > 0 )
if(get(handles.checkbox1,'Value') == 1)    
 I = Inscrita(Funcion,A,B,N)
 I1 = ['Area por poligonos inscritos ->  ' char(I) ' U²']
 set(handles.text5, 'String',I1)
else
 set(handles.text5,'String','Area por poligonos inscritos -> ?')    
end
if(get(handles.checkbox2,'Value') == 1)
   C  = Circuinscrita(Funcion,A,B,N)
 C1 = ['Area por poligonos circunscritos ->  ' char(C) ' U²'] 
 set(handles.text6, 'String',C1)
else
 set(handles.text6, 'String','Area por poligonos circunscritos -> ?')    
end  
if(get(handles.checkbox3,'Value') == 1)
   R  = Rieman(Funcion,A,B,N)
 R1 = ['Area por suma de Riemann ->  ' char(R) ' U²'] 
 set(handles.text7, 'String',R1)
else
 set(handles.text7, 'String','Area por suma de Riemann -> ?')    
end 

else
    msgbox('N tiene que ser mayor que 0', 'Error','error');
end
else
    msgbox('Aun no ha escrito todos los datos', 'Error','error');    
end

